DATAS SEGMENT
	CONTENT DB 15,?,16 DUP(?)
    MSG DB 'INPUT TEXT: $'
    UPPER DB 0
    LOWER DB 0
DATAS ENDS

CODES SEGMENT
    ASSUME CS:CODES,DS:DATAS
START:
    MOV AX,DATAS
    MOV DS,AX
    
    ;清屏
    MOV AH,6
	MOV AL,0
	MOV BH,07H  ;黑底白字
	MOV CH,0	;左上(0,0)
	MOV CL,0
	MOV DH,24
	MOV DL,79	;右下(24,79)->25 * 80
	INT 10H
	
	;打开窗口
	MOV AH,6
	MOV AL,0
	MOV BH,70H
	MOV CH,5
	MOV CL,10
	MOV DH,14
	MOV DL,50
	INT 10H
	
	;移动光标到5行10列
	MOV AH,2
	MOV DH,5
	MOV DL,10
	MOV BH,0
	INT 10H
	
	LEA DX,MSG
	MOV AH,9
	INT 21H
	
	MOV AL,0
	MOV AH,10
	LEA DX,CONTENT
	INT 21H
	
	;判断大小写
	MOV CL,CONTENT[1]
	MOV SI,0
   LET0:
    CMP CL,0
    JE LETE
	MOV AL,CONTENT[2+SI]
	TEST AL,20H
	JE LET1
	INC LOWER
	INC SI
	DEC CL
    JMP LET0
   LET1:
    INC UPPER
    INC SI
    DEC CL 
    JMP LET0
    
   LETE:
   ;移动光标到6行10列
	MOV AH,2
	MOV DH,6
	MOV DL,10
	MOV BH,0
	INT 10H
	
	MOV DL,UPPER
	ADD DL,30H
	MOV AH,2
	INT 21H
	
	;移动光标到7行10列
	MOV AH,2
	MOV DH,7
	MOV DL,10
	MOV BH,0
	INT 10H
	
	MOV DL,LOWER
	ADD DL,30H
	INT 21H
	
    MOV AH,4CH
    INT 21H
CODES ENDS
    END START


