DATAS SEGMENT
    LIST1 DB -1,-2,-3,-4,-5
    LIST2 DB -2,-4,-6,-8,-10
    SUM1 DB 0
    SUM2 DB 0
DATAS ENDS

STACKS SEGMENT
STACKS ENDS

CODES SEGMENT
    ASSUME CS:CODES,DS:DATAS,SS:STACKS
START:
    MOV AX,DATAS
    MOV DS,AX
    
    MOV SI,LIST2-LIST1
    LEA DI,LIST1
    CALL GETSUM
    MOV SUM1,AH
    
    MOV SI,SUM1-LIST2
    LEA DI,LIST2
    CALL GETSUM
    MOV SUM2,AH
    
    MOV AH,4CH
    INT 21H
    
    GETSUM PROC
      MOV AH,0
      DEC SI
     LET0:
      CMP SI,-1
      JZ LETE
      MOV AL,[DI]
      ADD AH,AL
      INC DI
      DEC SI
      JMP LET0
     LETE:
      RET
    GETSUM ENDP
CODES ENDS
    END START
