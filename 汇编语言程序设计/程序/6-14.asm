DATAS SEGMENT
    STRG1 DB 'HELLO WORLD!$'
    N DB ?
DATAS ENDS

EXTRAS SEGMENT
	STRG2 DB 20 DUP(?)
EXTRAS ENDS

CODES SEGMENT
    ASSUME CS:CODES,DS:DATAS,ES:EXTRAS
START:
    MOV AX,DATAS
    MOV DS,AX
    MOV AX,EXTRAS
    MOV ES,AX
    
    ;获取长度赋给CX
    MOV AH,0
    MOV AL,N-STRG1
    MOV CX,AX
    
    LEA SI,STRG1
    LEA DI,STRG2
    CLD
    ;传送
    REP MOVSB
    
    MOV AH,4CH
    INT 21H
CODES ENDS
    END START

